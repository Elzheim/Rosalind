def gccontent(raw):
    items = raw.split('>')
    max_gc = 0
    max_gcID =''
    for fa in items[1:]:
        fa_items= fa.split('\n')
        seq_name = fa_items[0]
        seq = ''.join(fa_items[1:]).rstrip()
        seq_len = len(seq)
        gc_count = 0
        for nt in seq :
            if nt == 'C': gc_count+=1
            if nt == 'G' : gc_count+=1
        gc_content = (gc_count/seq_len)*100
        if gc_content >max_gc:
            max_gc = gc_content
            max_gcID = seq_name

    print(max_gcID)
    print(max_gc)

    return (max_gcID,max_gc)


